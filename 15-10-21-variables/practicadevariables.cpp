#include <iostream>

int main()
{
	float x;
	bool a = false;
	double y;
	float z = 1.0e40;
	std::cout << "x = " << x << "\n";
	std::cout << "a = " << a << "\n";
	std::cout << "y = " << y << "\n";
	std::cout << "Dirección de memoria y: " << &y << "\n";
	std::cout << "Bytes que ocupa y:  " << sizeof(y) << "\n";
	std::cout << "z = " << z << "\n";

	return 0;
}

// El programa no puede mostrar valores con tantas cifras (en este caso 40 ceros) porque se usó el tipo de variable float, la cual o tiene tanta precisión.
